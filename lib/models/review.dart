import 'dart:math';
import './review_user.dart';

Random random = Random();

class Review {
  final ReviewUser reviewUser;
  final int reviewNo;
  final String content;
  final int star;
  final DateTime regDtm;
  final int likeCnt;
  final List<String> imgs;
  final List<int> tags;
  final bool isLike;

  Review({
    required this.reviewUser,
    required this.reviewNo,
    required this.content,
    required this.star,
    required this.regDtm,
    required this.likeCnt,
    required this.imgs,
    required this.tags,
    required this.isLike,
  });

  factory Review.fromJson(Map<String, dynamic> json) {
    return Review(
      reviewUser: ReviewUser.fromJson(json),
      reviewNo: json['postId'],
      content: json['body'],
      star: random.nextInt(5) + 1,
      regDtm: DateTime.now(),
      likeCnt: random.nextInt(200),
      imgs: _dummyImgs,
      isLike: true,
      tags: List.generate(random.nextInt(5), (index) => random.nextInt(15)),
    );
  }
}

